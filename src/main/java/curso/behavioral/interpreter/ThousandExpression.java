package curso.behavioral.interpreter;

public class ThousandExpression extends Expression {

	@Override
	protected String nove() {
		return " ";
	}

	@Override
	protected String cinco() {
		return " ";
	}

	@Override
	protected String quatro() {
		return " ";
	}

	@Override
	protected String um() {
		return "M";
	}

	@Override
	protected int multiplo() {
		return 1000;
	}
	
}